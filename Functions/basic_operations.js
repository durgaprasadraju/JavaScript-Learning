function add (sahas,bujji) 
{
    const sum = sahas + bujji;
    return sum;
}
console.log(add(89,11))




function subract (karna,ashwatama)
{
    const subract = karna - ashwatama;
    return subract;
}
console.log(subract(198,97))



function multiply (sivaram,sravya)
{
    const multiply = sivaram * sravya;
    return multiply;
}
console.log(multiply(8,7))



function divide (bujji,bhairava)
{
    const divide = bujji / bhairava;
    return divide;
}
console.log(divide(16,8))